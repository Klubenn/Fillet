#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t	i;

	i = 0;
	if (s1 || s2)
	{
		if ((s1 && !s2) || (!s1 && s2))
			return (0);
		else
		{
			while (s1[i] && s2[i] && s1[i] == s2[i])
				i++;
			if (s1[i] == s2[i])
				return (1);
			else
				return (0);
		}
	}
	else
		return (1);
}
