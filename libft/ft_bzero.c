#include "libft.h"

void	ft_bzero(void *b, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	i = 0;
	tmp = (unsigned char*)b;
	while (n > 0)
	{
		n--;
		tmp[n] = 0;
	}
}
