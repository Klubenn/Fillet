#include "libft.h"

void	ft_putnbr(int n)
{
	long long nb;

	nb = (1 - 2 * (n < 0)) * (long long)n;
	if (n < 0)
		ft_putchar('-');
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchar((nb % 10) + '0');
}
